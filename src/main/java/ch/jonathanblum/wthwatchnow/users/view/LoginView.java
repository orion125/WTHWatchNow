    private void authenticate(int retStatus) {
        if(controller.authenticate((User) getSelectedUser(), new String(pfPassword.getPassword()))) {
            returnStatus = retStatus;
            setVisible(false);
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Le mot de passe ne correspond pas.", "Mot de passe incorrect",JOptionPane.ERROR_MESSAGE);
        }
    }

